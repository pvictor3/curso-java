
public class Operaciones {
    public double sumar(double x, double y){
        return x+y;
    }
    
    public double restar(double x, double y){
        return x-y;
    }
    
    public double mult(double x, double y){
        return x*y;
    }
    
    public double div(double x, double y){
        return x/y;
    }
}
