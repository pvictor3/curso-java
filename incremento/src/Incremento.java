
public class Incremento {
    
    public static void main(String[] args) {
        int c;
        
        c = 5;
        System.out.println("El valor de c es: " + c);
        System.out.println("El valor de c ahora es: " + c++);
        System.out.println("El valor de c despues del incremento: " + c);
    }
    
}
